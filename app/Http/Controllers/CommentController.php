     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Album $album,Photo $photo,Comment $comment)
    {
        if ($photo->isPhotoOwner() || $comment->user_id == auth()->id()){
            $comment->delete();
