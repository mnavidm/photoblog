    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,Album $album,Photo $photo)
    {
        $this->validate($request,['comment' => 'required']);

        $photo->comments()->create(array_merge($request->only('comment'),['user_id'=>Auth::id()]));

        return redirect(route('album.show',[$album]))->withErrors(['success'=>"Comment added successfully"]);

    }

